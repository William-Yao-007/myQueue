QUEUE_T uart_queue;


//=============================================================================
//=============================================================================
void init_uart(void)
{
    //

    init_queue(&uart_queue);
}

//=============================================================================
//=============================================================================
char uart_receive_byte(QUEUE_T *uq, char byte_value)
{
    return(enqueue(uq, byte_value));
}

//=============================================================================
//=============================================================================
short uart_receive_data(QUEUE_T *uq, char *byte_data, unsigned short byte_length)
{
    short i = 0;

    for(i = 0; i < byte_length; i++) {
        if(enqueue(uq, byte_data[i]) < 0) return -1;
    }

    return i;
}

